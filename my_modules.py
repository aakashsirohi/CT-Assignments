def house_area(l, w):
    """This module will calculate the square footage of a house 
    when the user will provide the length and width """
    house_area = l * w
    return (house_area)

def circ_circum(rad):
    """This module will calculate the circumference of a circle 
    when the user gives the radius."""
    pi = 22 / 7
    circumference = 2 * pi * rad
    return (circumference)